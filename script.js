function initNavigation() {
  const toggle = document.querySelector(".nav-toggle");
  const links = document.querySelector("#nav-links");

  toggle.addEventListener("click", () => {
    const isOpen = links.classList.toggle("open");
    toggle.setAttribute("aria-expanded", String(isOpen));
  });

  links.querySelectorAll("a").forEach((link) => {
    link.addEventListener("click", () => {
      links.classList.remove("open");
      toggle.setAttribute("aria-expanded", "false");
    });
  });
}

function initMonthFilters() {
  Object.entries(botViews).forEach(([viewKey, view]) => {
    const availableMonths = monthOrder.filter((month) => month in data[view.dataKey].months);
    view.filter.innerHTML = availableMonths
      .map((month) => `<option value="${month}">${month}</option>`)
      .join("");
    view.filter.value = availableMonths.includes(view.defaultMonth) ? view.defaultMonth : availableMonths[0];
    view.filter.addEventListener("change", () => renderBot(viewKey, view.filter.value));
  });
}

function summarize(rows) {
  if (!rows.length) {
    return { days: 0, avg: 0, total: 0, min: 0, max: 0 };
  }
  const values = rows.map((row) => row.value);
  const total = values.reduce((sum, value) => sum + value, 0);
  return {
    days: rows.length,
    avg: total / rows.length,
    total,
    min: Math.min(...values),
    max: Math.max(...values)
  };
}

function formatPercent(value) {
  return `${value.toFixed(2)}%`;
}

function renderKpis(kpiGrid, month, rows) {
  const summary = summarize(rows);
  const cards = [
    ["Selected Month", month],
    ["Reported Days", String(summary.days)],
    ["Average Daily", formatPercent(summary.avg)],
    ["Total Reported", formatPercent(summary.total)],
    ["Range", summary.days ? `${formatPercent(summary.min)} - ${formatPercent(summary.max)}` : "No data"]
  ];

  kpiGrid.innerHTML = cards
    .map(([label, value]) => `<article class="kpi"><span>${label}</span><strong>${value}</strong></article>`)
    .join("");
}

function renderChart(containerId, summaryId, rows, title, month, colorStart, colorEnd) {
  const container = document.querySelector(containerId);
  const summaryEl = document.querySelector(summaryId);
  const summary = summarize(rows);

  if (!rows.length) {
    summaryEl.textContent = "No daily entries";
    container.innerHTML = `<div class="empty-chart">No ${title} entries for ${month}</div>`;
    return;
  }

  summaryEl.textContent = `${summary.days} days | ${formatPercent(summary.total)} total`;

  const width = 720;
  const height = 300;
  const pad = { top: 20, right: 18, bottom: 42, left: 46 };
  const chartWidth = width - pad.left - pad.right;
  const chartHeight = height - pad.top - pad.bottom;
  const maxValue = Math.max(summary.max, 1);
  const gap = 4;
  const barWidth = Math.max(8, (chartWidth - gap * (rows.length - 1)) / rows.length);
  const ticks = [0, maxValue / 2, maxValue];

  const grid = ticks.map((tick) => {
    const y = pad.top + chartHeight - (tick / maxValue) * chartHeight;
    return `
      <line x1="${pad.left}" y1="${y}" x2="${width - pad.right}" y2="${y}" stroke="#d7dee8" stroke-width="1" />
      <text x="${pad.left - 10}" y="${y + 4}" text-anchor="end" fill="#65758a" font-size="12">${tick.toFixed(1)}%</text>
    `;
  }).join("");

  const bars = rows.map((row, index) => {
    const x = pad.left + index * (barWidth + gap);
    const barHeight = (row.value / maxValue) * chartHeight;
    const y = pad.top + chartHeight - barHeight;
    const showLabel = rows.length <= 16 || index % 3 === 0 || index === rows.length - 1;
    return `
      <rect x="${x}" y="${y}" width="${barWidth}" height="${barHeight}" rx="4" fill="url(#${title.toLowerCase()}Gradient)">
        <title>Day ${row.day}: ${formatPercent(row.value)}</title>
      </rect>
      ${showLabel ? `<text x="${x + barWidth / 2}" y="${height - 16}" text-anchor="middle" fill="#65758a" font-size="11">${row.day}</text>` : ""}
    `;
  }).join("");

  container.innerHTML = `
    <svg class="bar-chart" viewBox="0 0 ${width} ${height}" role="img" aria-label="${title} daily percentage bar chart for ${month}">
      <defs>
        <linearGradient id="${title.toLowerCase()}Gradient" x1="0" x2="0" y1="0" y2="1">
          <stop offset="0%" stop-color="${colorStart}" />
          <stop offset="100%" stop-color="${colorEnd}" />
        </linearGradient>
      </defs>
      <rect x="0" y="0" width="${width}" height="${height}" rx="8" fill="#ffffff" />
      ${grid}
      <line x1="${pad.left}" y1="${pad.top + chartHeight}" x2="${width - pad.right}" y2="${pad.top + chartHeight}" stroke="#aab6c5" stroke-width="1" />
      ${bars}
      <text x="${width / 2}" y="${height - 2}" text-anchor="middle" fill="#334155" font-size="12" font-weight="700">Day of month</text>
    </svg>
  `;
}

function renderBot(viewKey, month) {
  const view = botViews[viewKey];
  const rows = data[view.dataKey].months[month] || [];
  renderKpis(view.kpiGrid, month, rows);
  renderChart(view.chart, view.summary, rows, view.title, month, view.colors[0], view.colors[1]);
}

initNavigation();
initMonthFilters();
Object.entries(botViews).forEach(([viewKey, view]) => renderBot(viewKey, view.filter.value));
